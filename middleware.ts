import { createServerClient } from '@supabase/ssr'
import { NextResponse, type NextRequest } from 'next/server'

export async function middleware(request: NextRequest) {
  const response = NextResponse.next({
    request: {
      headers: request.headers,
    },
  })

  // 1. Debug: Check Env Vars to prevent immediate crash
  const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL
  const supabaseKey = process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY

  if (!supabaseUrl || !supabaseKey) {
    console.error('❌ CRITICAL ERROR: Supabase Environment Variables are MISSING in Middleware!')
    console.log('URL Status:', supabaseUrl ? 'Defined' : 'Missing')
    console.log('KEY Status:', supabaseKey ? 'Defined' : 'Missing')
    // Return simple response to prevent 500 Error
    return response
  }

  try {
    // 2. Safe Client Creation
    const supabase = createServerClient(
      supabaseUrl,
      supabaseKey,
      {
        cookies: {
          getAll() {
            return request.cookies.getAll()
          },
          setAll(cookiesToSet) {
            cookiesToSet.forEach(({ name, value, options }) => request.cookies.set(name, value))
            cookiesToSet.forEach(({ name, value, options }) =>
              response.cookies.set(name, value, options)
            )
          },
        },
      }
    )

    // 3. Safe Session Check
    await supabase.auth.getSession()

  } catch (error) {
    console.error('❌ Supabase Client Execution Failed:', error)
  }

  return response
}

export const config = {
  matcher: [
    '/((?!_next/static|_next/image|favicon.ico|.*\\.(?:svg|png|jpg|jpeg|gif|webp)$).*)',
  ],
}
